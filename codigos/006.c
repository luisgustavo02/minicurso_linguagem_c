#include<stdio.h>
#include<math.h>

int main(){
	/*
	A funcao ceil() faz o arredondamento para cima
	
	Enquanto a funcao floor() faz o arredondamento para baixo
	*/
	printf("ceil(4.7) = %f\tceil(4.4) = %f\n", ceil(4.7), ceil(4.4));
	printf("floor(4.7) = %f\tfloor(4.4) = %f", floor(4.7), floor(4.4));
	
	return 0;
}
