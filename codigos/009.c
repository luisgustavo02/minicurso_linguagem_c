#include<stdio.h>
#include<ctype.h>

int main(){
	// Algumas das principais funcoes da biblioteca ctype
	printf("toupper('g') = %c\n", toupper('g'));
	printf("toupper('G') = %c\n\n", toupper('G'));
	printf("tolower('g') = %c\n", tolower('g'));
	printf("tolower('G') = %c\n", tolower('G'));
	
	return 0;
}
