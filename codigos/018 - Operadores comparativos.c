#include<stdio.h>

int main(){
	// Comparando valores
	int num1 = 1;
	int num2 = 3;
	printf("num1 == num2 => %d\n", num1 == num2);
	printf("num1 != num2 => %d\n", num1 != num2);
	printf("num1 < num2 => %d\n", num1 < num2);
	printf("num1 <= num2 => %d\n", num1 <= num2);
	printf("num1 > num2 => %d\n", num1 > num2);
	printf("num1 >= num2 => %d\n", num1 >= num2);
	
	return 0;
}
